#include <stdio.h>
#include <stdlib.h>

#define ARRSIZE 10

void OSSort(int arr[], int size) {
    #ifdef _WIN32_
        for (int i = 0; i < size - 1; i++) {
            for (int j = 0; j < size - i - 1; j++) {
                if (arr[j] > arr[j + 1]) {
                    int temp = arr[j];
                    arr[j] = arr[j + 1];
                    arr[j + 1] = temp;
                }
            }
        }
    #elif defined(__linux__)
        for (int i = 0; i < size - 1; i++) {
            int min_idx = i;
            for (int j = i + 1; j < size; j++) {
                if (arr[j] < arr[min_idx]) {
                    min_idx = j;
                }
            }
            int temp = arr[i];
            arr[i] = arr[min_idx];
            arr[min_idx] = temp;
        }
    #elif defined(__APPLE__)
        for (int i = 1; i < size; i++) {
            int key = arr[i];
            int j = i - 1;
            while (j >= 0 && arr[j] > key) {
                arr[j + 1] = arr[j];
                j = j - 1;
            }
            arr[j + 1] = key;
        }
    #else
        for (int i = 0; i < size / 2; i++) {
            int temp = arr[i];
            arr[i] = arr[size - i - 1];
            arr[size - i - 1] = temp;
        }
    #endif
}

int main() {
    int arr[] = {5, 2, 9, 3, 6, 1, 8, 4, 7, 10};

    OSSort(arr, ARRSIZE);

    for (int i = 0; i < ARRSIZE; i++) {
        printf("%d ", arr[i]);
    }

    return 0;
}
